public class Triangle { //triangle class to set the coords from x and y-axis of the vertices
    int xA, yA;
    int xB, yB;
    int xC, yC;

    public Triangle(int xA, int yA, int xB, int yB, int xC, int yC) {
        this.xA = xA;
        this.yA = yA;
        this.xB = xB;
        this.yB = yB;
        this.xC = xC;
        this.yC = yC;
    }
}
